export const dictionaries: Record<AppLocale, Record<MessageKey, string>> = {
  "pt-PT": ptPT,
  en,
};

export function normalizeLocale(value: string | null | undefined): AppLocale {
  return value === "en" ? "en" : "pt-PT";
}

export function formatMessage(
  locale: AppLocale,
  key: MessageKey,
  parameters?: Record<string, string | number>,
) {
  const message = dictionaries[locale][key];
  if (!parameters) return message;
  return message.replace(/\{(\w+)\}/g, (match, name: string) =>
    Object.prototype.hasOwnProperty.call(parameters, name)
      ? String(parameters[name])
      : match,
  );
}

